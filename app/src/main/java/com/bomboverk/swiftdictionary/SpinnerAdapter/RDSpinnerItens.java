package com.bomboverk.swiftdictionary.SpinnerAdapter;

public class RDSpinnerItens {

    private Long idDic;
    private String nomeDic;

    public RDSpinnerItens(Long idDic, String nomeDic) {
        this.idDic = idDic;
        this.nomeDic = nomeDic;
    }

    public Long getIdDic() {
        return idDic;
    }

    public String getNomeDic() {
        return nomeDic;
    }

}
